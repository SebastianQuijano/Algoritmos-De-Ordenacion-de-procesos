    private Timer t;
    private int h, m, s, cs;
    private ActionListener acciones = new ActionListener(){
        
        @Override
        public void actionPerformed(ActionEvent ae) {
            cs++;
            if (cs==100) {
                cs=0;
                ++s;
                if (!a.getArregloProcesos().isEmpty()) {
                    actualizarProcesos();
                }else{
                    error.setText("No hay procesos en espera.");
                    t.stop();
                    btnIniciar.setEnabled(true);
                    btnPausa.setEnabled(false);
                }
            }   
            if (s==60) {
                s=0;
                ++m;
            }
            if (m==60) {
                m=0;
                ++h;
            }
            actualizarLabel();
        }
        
    };
    
    private void actualizarTabla(){
        
        if (a.getSizeArray() < 15) {
                for (int i = 0; i < 15; i++) {
                    if (i < a.getSizeArray())
                        tabla.setValueAt(a.getProc(i).getNombre(), i, 0);
                    else
                        tabla.setValueAt("", i, 0);
                }
                for (int i = 0; i < 15; i++) {
                    if (i < a.getSizeArray())
                        tabla.setValueAt(a.getProc(i).getRafaga(), i, 1);
                    else
                        tabla.setValueAt("", i, 1);
                }
            }else{
                for (int i = 0; i < 15; i++) {
                    tabla.setValueAt(a.getProc(i).getNombre(), i, 0);
                }
                for (int i = 0; i < 15; i++) {
                    tabla.setValueAt(a.getProc(i).getRafaga(), i, 1);
                }
            }
    }
    
    
    
    private void actualizarLabel(){
        String tiempo = (h<=9?"0":"")+h+":"+(m<=9?"0":"")+m+":"+(s<=9?"0":"")+s+":"+(cs<=9?"0":"")+cs;
        etiquetaTiempo.setText(tiempo);
    }
    
    private void actualizarProcesos(){
        if ((a.getArregloProcesos().get(0).getRafaga()<=a.getQuantum())) {
            a.getArregloProcesos().remove(0);
            actualizarTabla();
        }else{
            a.getArregloProcesos().get(0).setRafaga(a.getArregloProcesos().get(0).getRafaga()-a.getQuantum());
            a.getArregloProcesos().add(a.getArregloProcesos().get(0));
            a.getArregloProcesos().remove(0);
            actualizarTabla();
        }
      
        
        
    }
    
    
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:
        a.AgregarProceso(txtNombre.getText(), Integer.parseInt(txtRafaga.getText()));
        
        if (a.getSizeArray() < 15) {
            for (int i = 0; i < a.getSizeArray(); i++) {
                tabla.setValueAt(a.getProc(i).getNombre(), i, 0);
            }
            for (int i = 0; i < a.getSizeArray(); i++) {
                tabla.setValueAt(a.getProc(i).getRafaga(), i, 1);
            }
        }else{
            for (int i = 0; i < 15; i++) {
                tabla.setValueAt(a.getProc(i).getNombre(), i, 0);
            }
            for (int i = 0; i < 15; i++) {
                tabla.setValueAt(a.getProc(i).getRafaga(), i, 1);
            }
        }
        error.setText("Proceso agregado.");
                     
    }                                          

    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:
        t.start();
        btnIniciar.setEnabled(false);
        btnIniciar.setText("Reanudar");
        btnPausa.setEnabled(true);
        btnDetener.setEnabled(true);
        error.setText("");

    }                                          

    private void btnPausaActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        t.stop();
        btnIniciar.setEnabled(true);
        btnPausa.setEnabled(false);
        error.setText("Pausa.");
    }                                        

    private void btnDetenerActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:
        if (t.isRunning()) {
            t.stop();
            btnIniciar.setEnabled(true);
        }
        btnIniciar.setText("Iniciar");
        btnPausa.setEnabled(false);
        btnDetener.setEnabled(false);
        h=0; m=0; s=0; cs=0;
        actualizarLabel();
        error.setText("Se ha detenido.");
        
    }                                          

